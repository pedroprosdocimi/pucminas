    public void PopularJTable() {
        Connection con = new ConnectionFactory().getConnection();
        String itemText = (String)jComboBox1.getSelectedItem( );
        String tipoconta = "-1";
        if(itemText=="Energia"){
            tipoconta="e";
        }else if(itemText=="Água"){
            tipoconta="a";
        }
        if(tipoconta=="-1"){
            JOptionPane.showMessageDialog(null, "Favor selecionar um Tipo de Conta");
        }else{
        
            String sql = "select distinct nomcli,valcon from cliente natural join conta where tipcon='"+tipoconta+"' and mescon="+jTextField2.getText()+" and anocon="+jTextField3.getText()+" order by nomcli";
            try
            {
             PreparedStatement banco = con.prepareStatement(sql);
              banco.execute(); // cria o vetor

             ResultSet resultado = banco.executeQuery(sql);

             DefaultTableModel model =(DefaultTableModel) jTable1.getModel();
             model.setNumRows(0);

             while(resultado.next())
             {
                 model.addRow(new Object[] 
                 { 
                    //retorna os dados da tabela do BD, cada campo e um coluna.
                    resultado.getString("nomcli"),
                    resultado.getString("valcon")
                 }); 
            } 
             banco.close();
             con.close();
            }
           catch (SQLException ex)
           {
              System.out.println("o erro foi " +ex);
            }
        }
    }
