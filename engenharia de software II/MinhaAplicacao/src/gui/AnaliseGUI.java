    public void PopularJTable() {
        Connection con = new ConnectionFactory().getConnection();
            int anoanterior = Integer.parseInt(jTextField2.getText())-1;
            String sql = "select sum(medatu) from conta where mescon="+Integer.parseInt(jTextField1.getText())+" and anocon="+Integer.parseInt(jTextField2.getText());
            String sql2 ="select sum(medatu) from conta where mescon="+Integer.parseInt(jTextField1.getText())+" and anocon="+anoanterior;
            try
            {
             PreparedStatement banco = con.prepareStatement(sql);
              banco.execute(); // cria o vetor
               PreparedStatement banco2 = con.prepareStatement(sql2);
              banco2.execute();
             ResultSet resultado = banco.executeQuery(sql);
             ResultSet resultado2 = banco2.executeQuery(sql2);
             resultado.next();
             resultado2.next();

             DefaultTableModel model =(DefaultTableModel) jTable1.getModel();
             model.setNumRows(0);
             int variacao = resultado.getInt("sum(medatu)") - resultado2.getInt("sum(medatu)");
             int perc;
             if(resultado2.getInt("sum(medatu)")!=0){
                perc = resultado.getInt("sum(medatu)")/ resultado2.getInt("sum(medatu)");
             }else{
                 perc=0;
             }
             String percentual = Integer.toString(perc)+"%";
                 model.addRow(new Object[] 
                 { 
                    //retorna os dados da tabela do BD, cada campo e um coluna.
                    jTextField1.getText(),
                    jTextField2.getText(),
                    variacao,
                    percentual
                 }); 
                 
             resultado.close();
             resultado2.close();
             banco.close();
             banco2.close();
             con.close();
            }
           catch (SQLException ex)
           {
              System.out.println("o erro foi " +ex);
            }
            
        
    }
